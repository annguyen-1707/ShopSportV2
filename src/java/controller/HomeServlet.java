    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        CategoryDAO cdao = new CategoryDAO();
        TypeDAO tdao = new TypeDAO();
        ProductDAO pdao = new ProductDAO();
        List<Category> categories = cdao.getAll();
        List<Type> types = tdao.getAll();

        //add cookie
        List<Product> productsAll = pdao.getAllProducts();
        Cookie[] arr = request.getCookies();
        String txt = "";
        if (arr != null) {
            for (Cookie o : arr) {
                if (o.getName().equals("cart")) {
                    txt += o.getValue();
                }
            }
        }
        Cart cart = new Cart(txt, productsAll);
        List<Item> listItem = cart.getItems();
        int n;
        if (listItem != null) {
            n = listItem.size();
        } else {
            n = 0;
        }
        HttpSession session = request.getSession();
        session.setAttribute("size", n);

        //add các sản phảm
        session.setAttribute("categories", categories);
        session.setAttribute("types", types);
        session.setAttribute("productsAll", productsAll);
        //sp ban chay nhat
        List<Product> productsTopBuy = pdao.topBuy(4);
        request.setAttribute("productsTopBuy", productsTopBuy);
        //sp moi nhat
        List<Product> productsNew = pdao.getProductTop(4, true, "dateRelease");
        request.setAttribute("productsNew", productsNew);
        //sp sale nhieu nhat
        List<Product> productsSale = pdao.getProductTop(4, true, "discount");
        request.setAttribute("productsSale", productsSale);
        request.getRequestDispatcher("home.jsp").forward(request, response);
    }

